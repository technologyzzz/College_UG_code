package petanicode;

public class p3_luas_lingkaran {
    public static void main(String args[]){
        //deklarasi var
        Double luas, pi;
        int r;

        //var input
        pi = 3.14;
        r = 14;

        //var proses
        luas = pi * r * r;

        //output
        System.out.print("Luas lingkaran adalah : " );
        System.out.println(luas);
    }
}